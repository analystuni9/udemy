# Exercicio de programação com if e comparação
primeiroValor = input("Digite um valor: ")
segundoValor = input("Digite outro valor: ")

if primeiroValor >= segundoValor:
    print(f"O primeiro valor '{primeiroValor}' é maior ou igual que o segundo valor '{segundoValor}'")
else:
    print(f"O segundo valor '{segundoValor}' é maior ou igual que o primeiro valor '{primeiroValor}'")
     