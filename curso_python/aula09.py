# Operadores aritimeticos
adicao = 10 + 10
print("Adição ", adicao)

subtracao = 10 - 5
print("Subtracao ", subtracao)

multiplicacao = 10 * 10
print("Multiplicacao ", multiplicacao)

divisao = 10 / 10 # -> sempre o retorno e float não importa se vc só colocou numero inteiro
print("Divisão ", divisao)

divisaoInteira = 10 // 2.2 # -> sempre corta as casas decimais
print("Divisão inteira ", divisaoInteira)

exponenciacao = 2 ** 10
print("Exponenciação ", exponenciacao)

modulo = 55 % 2 # -> resto da divisão
print("Modulo ", modulo)
