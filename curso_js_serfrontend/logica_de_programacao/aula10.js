// Operadores de comparação
// igualdade de valor ==
// igualdade de valor e tipo ===
// <, >, <=, >=
// != valores diferentes
// !== valores e tipos diferentes
let n1 = 10;
let n2 = 20;
let n3 = 30;
console.log(n1, n2, n3);
console.log(n1 == "10");
console.log(n1 === "10");
console.log(n2 < 10);
console.log(n1 >= 11);
console.log(n1 <= 11);
console.log(n1 != "10");
console.log(n1 !== "10");