// Operadores de atribuição
let n1 = 10;
// n1 = n1 + 15;
n1 += 15;
console.log(n1);
let n2 = 20;
// n2 = n2 -15;
n2 -= 15;
console.log(n2);
let n3 = 30;
// n3 = n3 * 15;
n3 *= 15;
console.log(n3);
let n4 = 40;
// n4 = n4 % 15;
n4 %= 15;
console.log(n4);
let n5 = 50;
// n5 ** n5 = 15;
n5 **= 15;
console.log(n5);